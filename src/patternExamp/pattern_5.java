package patternExamp;

public class pattern_5 
{
	public static void main(String[] args) 
	{
		//*
		//**
		//***
		int star=1;
		
		//4<=3
		
		for(int i=1;i<=3;i++)
		{
			for(int j=1;j<=star;j++)
			{
				System.out.println("*");
			}
			System.out.println();
			star++;
			
		}
		
	}

}
