package TypesofVariable;

public class demo1 
{
	//Example od Local & Global Variable
	
	int c=10;  //Global Variable
	
	public void m1()
	{
		int a=20;   //Local Variable
		
		System.out.println(a);
		System.out.println(c);
		
	}
	public void m2()
	{
		int b=30;  //local variable
		System.out.println(b);
		System.out.println(c);
	}

}
